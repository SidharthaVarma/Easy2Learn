<strong>CPR for children and infants</strong><br>


The CPR steps for children and infants are slightly different to the steps for adults, as below.<br><br>

<strong>Preparation steps</strong><br>
To perform CPR on an infant or child, use the following preparation steps:<br>

Step 1. Place them on their back and open their airways
Place the child or infant carefully on their back and kneel beside their chest. Tilt their head backward slightly by lifting their chin.
Open their mouth. Check for any obstruction, such as food or vomit. If it is loose, remove it. If it is not loose, do not touch it, as this may push it farther into their airways.<br>
Step 2. Check for breathing
Place your ear next to their mouth and listen for around 10 seconds. If you do not hear breathing, or you only hear occasional gasps, begin to administer CPR.
Changes in an infant’s breathing patterns are normal, as they usually have periodic breathing.
Keep monitoring their breathing and perform CPR if they stop breathing.
CPR steps<br>
Use the following steps to perform CPR on a child or infant:<br>
Step 3. Perform two rescue breaths
If the child or infant is not breathing, perform two rescue breaths with their head tilted backward and their chin raised.
For a child, pinch their nose shut and place your mouth over theirs. Breathe into their mouth twice.
For an infant, place your mouth over their nose and mouth and blow for 1 second to make their chest rise. Then, deliver two rescue breaths.
If they are still unresponsive, begin chest compressions.<br>
Step 4. Perform 30 chest compressions
Kneel beside the child or infant.
For a child, use one of your hands. Place the heel of the hand at their sternum, which is in the center of the chest, between and slightly below their nipples. Press down hard and fast around 2 inches deep, or one-third the depth of the chest, at least 100 times per minute.
For an infant, use two fingers. Place your fingers in the center of their chest, between and slightly below the nipples. Perform 30 quick compressions around 1.5 inches deep.<br>
Step 5. Repeat
Repeat the cycle of rescue breaths and chest compressions until the child starts breathing or help arrives.

When to use CPR and when not to
Performing CPR when a person is not breathing can help prevent brain damage.
Use CPR when an adult is not breathing at all. For a child or infant, use CPR when they are not breathing normally. Always use CPR if the adult or child is not responding when you talk to them or tap them.

If someone is not breathing, giving CPR can ensure that oxygen-rich blood reaches the brain. This is important, as without oxygen, someone can sustain permanent brain damage or die in under 8 minutes.

A person might need CPR if they stop breathing in any of the following circumstances:

a cardiac arrest or heart attack
choking
a road traffic accident
near-drowning
suffocation
poisoning
a drug or alcohol overdose
smoke inhalation
electrocution
suspected sudden infant death syndrome
Only perform CPR if the adult is not breathing, or in children and infants, when they are not breathing normally, and their blood is not circulating. This is why it is important to ensure that the person does not respond to verbal or physical calls to attention before starting the CPR process.<br><br>

<strong>Summary</strong>
CPR is a life-saving first aid procedure. It can significantly improve someone’s changes of surviving if they suffer a heart attack or stop breathing following an accident or trauma.

The steps vary depending on whether the person is an infant, child, or adult. However, the basic cycle of chest compressions and rescue breaths will remain the same.

Only use CPR when an adult has stopped breathing. Check the person to see whether they respond to verbal or physical stimuli before starting CPR.




