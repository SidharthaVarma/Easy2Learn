      <strong>HOW TO REACT DURING AN EMERGENCY SITUATION</strong><br>
Whether it’s a natural disaster, a health emergency, or a threat in a public setting, here are tips on how best to react and safely “resolve” the emergency as much as possible. In any crisis, cool heads prevail. As difficult as it may be to stay calm, it gives you time to think and puts you in a better position to make intelligent suggestions that can sometimes be a matter of life and death.<br><br>

<strong>Assess the Situation</strong><br>
Consider the nature of the emergency. With so many different ways to respond, it’s essential to make the right choice. For example, in the case of a fire, flood or tornado, the solution is to get away from the threat, but for a fire, you want to get out of the building In a flood, you want to get to higher ground. In a tornado, getting below the ground is generally safer. None of these options is interchangeable.<br><br>

<strong>Determine A Course Of Action</strong><br>
Once you understand the nature of the emergency, it’s time to act. If you need to leave the building, follow the company’s emergency evacuation plan and practice. Your company is likely to have procedures in place that lay out what you should do so you don’t have to reinvent the wheel.<br><br>

<strong>Get To Safety</strong><br>
Moving to a safe location is the most important first step. With any luck, your company conducts drills so that you are well practiced in proper evacuation procedures. If they don’t, it’s worth suggesting. If there is no structured process in place, be sure you are aware of multiple exit routes from every part of the building.<br><br>

<strong>See Who Needs Help</strong><br>
Check if anyone needs help making their way to safety. Once everyone is clear, determine if anyone needs first aid of any sort and who is qualified to provide it. Severe injuries such as bleeding or head injuries require immediate attention. Note where each person requiring medical care is located so you can inform first responders when they arrive.<br><br>

<strong>Notify Rescue Personnel</strong>
Call 911 as soon as possible. Give them as many details as possible regarding the incident, any continuing danger and the condition of any victims. Thorough and accurate information gives them a better idea of how many rescue personnel or vehicles are needed. In the case of a very large event, they notify hospitals of what they should expect. The dispatcher may want you to remain on the line until help arrives so they can ask additional questions and you can let them know if conditions change.

<strong>Practice Daily Calm</strong><br>
Some swear by practices like meditation, while others don’t take it seriously, but responding to daily stressors by stepping back, evaluating the situation and taking a few deep breaths can make remaining calm in a genuine emergency second nature. Also, keep in mind that deep breathing gets more oxygen to your brain, giving you a real physiological advantage when you need to respond.<br><br>

<strong>Additional Advice:</strong><br>
<li>Don’t waste time. Staying calm doesn’t mean moving slowly.</li>
<li>Never assume an alarm is just a drill. Act as if every threat is real and respond promptly.</li>
<li>Familiarize yourself with company emergency plans ahead of time.</li>
<li>Leave possessions behind. Things can be replaced; people can’t.</li><br><br>





