<strong>Top 10 Engineering Entrance Exams in India 2023</strong><br><br>
<b>1. JEE Main 2024</b>
<p>
National Testing Agency (NTA) will conduct the Joint Entrance Examination (JEE) Main 2023 in two phases January and April. JEE Mains 2023 held in computer based mode (except the drawing section of Paper 2 (B.Arch) which is a pen and paper based exam). JEE Main is the admission procedure into the well-known IIITs, NITs and other more prestigious engineering institutes of the nation. More than 12 lakh applicants appear for this examination hoping to clear and reach JEE Advanced for admissions. The duration of the exam is 3 hours long and is multiple choice in nature. The examination is divided into two papers – Paper 1 is for B.Tech/B.E and Paper 2 is for B.Arch or B.Planning.
</p>
<b>Admissions Offered into: </b>31 NITs, 25 IIITs and 28 GFTIs<br><br>

<b>2. BITSAT 2024</b> <p>
The Birla Institute of Technology and Science (BITS) will conduct the University level engineering entrance exam, BITSAT 2024 in online mode. BITSAT (Birla Institute of Technology and Science Admission Test) offers admission to all eligible applicants into the three campuses of BITS. Around 2 lakh applicants appear for this examination every year making it a very sought after the entrance exam. The duration of the online examination is 3 hours and the objective based BITSAT question paper is divided into four sections – Physics, Chemistry, English Proficiency and Logical Reasoning and finally Mathematics. Around 2,000 seats are available for admission.

</p>

<b>Admissions Offered Into:</b>  BITS Pilani, BITS Goa and BITS Hyderabad.<br><br>

<b>3. MET 2024</b> <p>
Manipal Academy of Higher Education (earlier known as Manipal University) is responsible for conducting the annual online entrance examination for admissions into the institute. The Manipal Entrance Test (MET), previously known as MU OET, is a University level examination that brings in over 50,000 applicants per year. Admissions are provided in 16 engineering courses through MU OET and are open to all eligible applicants. The entrance exam is held in online mode and is 150 minutes long. The questions are objective multiple choice ones and the applicants will have to qualify this examination for admission into the institute.

</p>

<b>4. VITEEE 2024</b> <p>
Around 5000 undergraduate engineering seats are available for eligible applicants under VITEEE (VIT Engineering Entrance Exam). The VIT University conducts the University level examination and around 2 lakh students appear for it every year. The mode of examination is online and will be objective in nature. Within the duration of 2½ hours, the applicants will have to solve 125 questions from Physics, Mathematics, Chemistry, Aptitude and English sections.

</p>

<b>Admissions Offered Into: </b> VIT Chennai, VIT Vellore, VIT Andhra Pradesh and VIT Bhopal.<br><br>



<b>5. SRMJEEE 2024</b> <p>
The SRM Joint Engineering Entrance Examination (SRMJEEE) will be conducted by the SRM Institute of Science and Technology for all eligible aspirants. The SRMJEE 2024 exam will be held in computer-based mode for admissions into the 7,000 undergraduate engineering seats available. Every year, around 1 lakh students apply for the entrance examination. The entrance examination consists of 125 questions and the applicants will have to qualify for the exam for admission into the University. Direct admissions are provided to the top 1000 IIT JEE Rank holders along with the top rank holders of central and state boards of examinations.

<br><b>Admissions Offered Into: </b> SRM University Kattankulathur, SRM University Ramapuram, SRM University Ramapuram Par – Vadapalani and SRM University Ghaziabad.

<br><br><b>6. COMEDK UGET 2024</b> 
Admissions into the engineering courses offered by the Associations of Medical, Dental, and Engineering private colleges in the state of Karnataka is done through COMEDK UGET conducted by the Consortium of Medical Engineering and Dental Colleges of Karnataka (COMEDK). The entrance examination will be held across various exam centres in India. Around 20,000 B.Tech seats are offered for admission under B.Tech. The examination is 3 hours long and is based on the syllabus of the 10+2 standard. The applicants will have to solve 180 multiple choice questions to be eligible for admission.

<br><b>Admissions Offered Into: </b>Around 181 engineering institutes affiliated to COMEDK UGET <p>



<b>7. KIITEE 2024</b> <br>
The Kalinga Institute of Industrial Technology will be conducted authority of KIITEE. The examination is held annually for admissions of eligible applicants into the 4-year B.Tech programmes offered by the institute. KIITEE is conducted in online mode and has 3 sections – Physics, Chemistry, and Mathematics. The qualifying marks have to be obtained by the applicants for admission. Around 60 exam centres are available for the applicants appearing for KIITEE.



<br><b>Admissions Offered Into: </b> Kalinga Institute of Industrial Technology<br><br>

<b>8. SITEEE 2024</b><br>
Symbiosis Institute of Technology Engineering Entrance Examination (SITEEE) will conduct annually by Symbiosis Institute of Technology (SIT). The applicants who qualify SITEEE will be offered admission into the institute. JEE Main and MHT CET qualified applicants are also eligible for admission. The University level entrance exam offers admission to eligible applicants into the 420 available B.Tech seats. The entrance exam consists of 100 questions and the total marks is 200. The duration of SITEEE is 150 minutes.


<br><b>Admissions Offered Into: </b> Symbiosis Institute of Technology (SIT)<br><br>

<b>9. AMUEEE 2024</b><br> 
AMUEEE is a University level entrance examination will conduct by Aligarh Muslim University for admissions into the available B.Tech programmes. Based on the merit and rank obtained in the entrance exam, the eligible applicants are offered admission. AMUEEE is held over a duration of 3 hours and comprises of 150 questions from Physics, Chemistry and Mathematics. All questions are objective based.



<br><b>Admissions Offered Into: </b> Aligarh Muslim University


<br><br><b>10. MHT CET 2024</b> <br>
State Common Entrance Test Cell, Mumbai will conduct the MHT CET 2023 or Maharashtra Common Entrance Test for admissions to first-year of BE/BTech courses in Maharashtra state. It will be a pen and paper based engineering entrance test. Around 4 lakh students appear in MHT CET every year. Class 12 students having domicile of Maharashtra will be eligible to apply for MHT CET 2023. Candidates have to cover the Maha HSC syllabus to prepare for MHT CET.

